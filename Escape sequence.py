#Escape sequence

mensagem = 'aprender python é \nmuito divertido'

print(mensagem)

# \n é usado para fazer a frase dali em diante, ir para a linha de baixo.

mensagem2 = 'Coluna1\tColuna2\tColuna3'# (\t) para criar uma tabulação.
print(mensagem2)

'''Se você quiser mostrar um caminho de um arquivo, use (\\) duas barras invertidas, que no
print ficará (\) uma barra só'''

mensagem3 = 'c:\\user\\login\\desktop\\Henrique'
print(mensagem3)

mensagem4 = 'Aprender \'python\' é muito bom' #Use \'algo\' para poder colocar aspas na frase sem interferir no código
print(mensagem4)