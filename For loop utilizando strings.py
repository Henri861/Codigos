#For loop utilizando strings
palavra = 'Google'
for letra in palavra:
    print(letra + ' está dentro da palavra Google!')# Se quiser, use a (f') para colocar mais coisas{}.

for letra in palavra:
    print(f'{letra} está dentro da palavra {palavra}')

'''Você pode usar a variável para colocar na range,
ou pode colocar a palavra diretamente lá'''

